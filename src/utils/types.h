#ifndef TYPES_H_
#define TYPES_H_

#include <stdint.h>  // Include standard integer types for uint8_t, uint16_t, etc.

// Define unsigned integer types
typedef uint8_t u8_t;   /**< 8-bit unsigned integer type */
typedef uint16_t u16_t; /**< 16-bit unsigned integer type */
typedef uint32_t u32_t; /**< 32-bit unsigned integer type */
typedef uint64_t u64_t; /**< 64-bit unsigned integer type */

// Define signed integer types
typedef int8_t i8_t;   /**< 8-bit signed integer type */
typedef int16_t i16_t; /**< 16-bit signed integer type */
typedef int32_t i32_t; /**< 32-bit signed integer type */
typedef int64_t i64_t; /**< 64-bit signed integer type */

#endif  // !TYPES_H_
